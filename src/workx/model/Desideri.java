package workx.model;

public class Desideri {
	String id,utente,annuncio;

	public Desideri() {
		id = null;
		utente = null;
		annuncio = null;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUtente() {
		return utente;
	}

	public void setUtente(String utente) {
		this.utente = utente;
	}

	public String getAnnuncio() {
		return annuncio;
	}

	public void setAnnuncio(String annuncio) {
		this.annuncio = annuncio;
	}
}
